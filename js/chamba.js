	function editList(id) {
	    
	     
	     var wordObj = {"id":id};
		$("#edit").dialog({autoOpen:true});
		$.ajax({
			type: "POST",
			data: wordObj,
			url: "edit_enrollee.php",
			success: function(data){
				var obj = JSON.parse(data);
				
				$("input[name = 'e_id']").val(obj.id);
				$("input[name = 'e_firstname']").val(obj.firstname);
				$("input[name = 'e_lastname']").val(obj.lastname);
				$("select[name='e_gender']").val(obj.gender);
				$("input[name='e_age']").val(obj.age);
				$("input[name='e_address']").val(obj.address);
				$("input[name='e_number']").val(obj.number);


	},
			error: function(){}
		});
	
	}	


